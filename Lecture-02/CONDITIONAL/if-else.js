const status = 200;

if (status === 200) {
    console.log('OK!');
} else if (status === 400) {
    console.log('Error!');
} else {
    console.log('Unknown status');
}

//1. การประกาศตัวแปร const status = 200; 
// -ใช้ const เพื่อประกาศตัวแปรชื่อ status และกำหนดค่าเริ่มต้นเป็น 200
// -const หมายความว่าค่าของตัวแปรนี้จะไม่สามารถเปลี่ยนแปลงได้ในภายหลัง
//**
//2. การใช้คำสั่งเงื่อนไข (if...else if...else)
// โค้ดนี้ใช้คำสั่งเงื่อนไขเพื่อเช็คค่าในตัวแปร status:
// if (status === 200) {
//     console.log('OK!');
// }
// ตรวจสอบว่า status มีค่าเท่ากับ 200 หรือไม่ โดยใช้การเปรียบเทียบแบบ เข้มงวด (===)
// -ถ้าเงื่อนไขเป็นจริง (true) จะทำงานในบล็อกนี้และพิมพ์ "OK!"
// -ผลลัพธ์: OK!
//**
// else if (status === 400) {
//     console.log('Error!');
// }
// ถ้าเงื่อนไขแรก (status === 200) เป็นเท็จ (false), จะตรวจสอบเงื่อนไขถัดไปว่า status === 400 หรือไม่
// -ถ้าเงื่อนไขนี้เป็นจริง จะพิมพ์ "Error!"
// -ในกรณีนี้ เงื่อนไขนี้จะ ไม่ทำงาน เพราะ status เท่ากับ 200
//**
// else {
//     console.log('Unknown status');
// }
// ถ้าเงื่อนไขทั้งหมดที่ตรวจสอบก่อนหน้า (status === 200 และ status === 400) เป็นเท็จทั้งหมด จะเข้ามาทำงานในส่วนนี้
// -ใช้สำหรับจัดการกับค่าที่ไม่ได้ถูกตรวจสอบไว้ในเงื่อนไข
// -ในกรณีนี้ ส่วนนี้จะ ไม่ทำงาน เพราะเงื่อนไขแรกเป็นจริง
//**
// การทำงานทั้งหมดของโค้ด:
// 1.ตัวแปร status ถูกกำหนดค่าเป็น 200
// 2.ตรวจสอบเงื่อนไข:
//                 -status === 200 เป็นจริง → พิมพ์ "OK!"
//                 -ส่วนอื่น (else if และ else) จะถูกข้ามไป
// ผลลัพธ์สุดท้ายที่แสดงคือ : OK!






